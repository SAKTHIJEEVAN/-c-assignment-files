#include <stdio.h>

int main() {
    int integer = 10;
    float floating = 5.5;
    char character = 'A';
    double dbl = 123.456789;
    printf("Integer: %d\n", integer);
    printf("Float: %.2f\n", floating);
    printf("Character: %c\n", character);
    printf("Double: %.6lf\n", dbl);
    return 0;
}
